# want to access elements of list acc to index position
lst_name = ['Aesha' , 'Heer' , 'Meera' , 'Pinky']
print(lst_name)
total_name = len(lst_name)
for i in range(total_name):
    print(lst_name[i])

# Using Negative index
print(lst_name[-2])

# same access elements of list acc to negative index
lst_name = ['Aesha' , 'Heer' , 'Meera' , 'Pinky']
print(lst_name)
total_name = len(lst_name)
for i in range(total_name-1, -1 , -1):
    print(lst_name[i])

